// Contoh program JavaScript dengan pernyataan while

var i = 1;

// Looping dengan while hingga i mencapai 5
while (i <= 5) {
  console.log("Iterasi ke-" + i);
  i++;
}

// Contoh program JavaScript dengan pernyataan do-while

var j = 1;

// Looping dengan do-while hingga j mencapai 5
do {
  console.log("Iterasi ke-" + j);
  j++;
} while (j <= 5);






